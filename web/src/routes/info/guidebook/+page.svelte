    <h3>기온</h3>
    <p>
      이글거리는 태양에 화상을 입을 것이라는 걱정은 하지 않아도 된다. 대만은
      아열대기후지역에 속하기 때문에 태양빛이 밝기만큼이나 뜨거운 것은 아니다. <br
      />
      게다가, 대만은 사면이 바다로 둘러 싸여 있으며 선선한 해풍이 자주 불고 습도가
      높은 특징 때문에 건조한 한대지방에서 온 여행객들에게는 최적의 여행 장소라고
      할 수 있다. <br />
      선선한 기후는 더위에 찌든 해외 관광객들에게 일상탈출의 기회를 제공함은 물론
      하이킹을 하면서 마시는 대만의 맑고 신선한 공기는 대자연과 하나가 되는 자신의
      모습을 발견하게 한다.
    </p>
    <p>
      대만은 연중 따뜻한 기온분포를 보인다. 봄과 겨울에는 날씨의 변동이 심한
      편이지만 여름과 가을은 상대적으로 안정된 기온 변화를 보인다. 여행하기에
      좋은 최고의 관광지로서 <br />
      연중 평균 기온은 약 22℃이며 최저기온은 약 12℃에서 17℃의 분포를 보인다. 겨울에
      눈을 볼 수 있는 몇몇 산악지역을 제외하고 대만 전지역에서 눈을 보는 것은 매우
      힘들며 <br />
      봄이 지나고 여름이 들어설 무렵(3월에서 5월 사이)에는 때때로 이슬비가 땅을 촉촉이
      적셔주기도 한다. 이 시기에 대만을 방문하고자 하는 여행객들은 반드시 우산을
      가지고 다니는 것이 좋다. <br />
      여름(6월~8월)시즌에는 가끔씩 태풍의 영향을 받기도 한다. 특히 이 기간동안에는
      태풍의 영향으로 파도가 높게 일기 때문에 항상 일기예보에 귀를 기울여야 한다.
    </p>
    <p>
      대만의 가을(9월~10월)은 천고마비의 계절로 시원하고 쾌적한 날씨가 계속되며,
      상대적으로 따뜻하고 짧은 겨울(11월~2월)은 아름답게 물든 단풍나무를
      감상하기에 좋은 기간이다. <br />
      겨울에 가끔씩 불어 닥치는 한파는 따뜻한 날씨에서 지내온 대만인들에게 좋은 활력소
      역할을 한다. 말하자면 대만은 연중 봄날의 따스함과 포근함을 지닌 최고의 관광지로써
      관광객을 <br />
      모실 준비가 항상 되어있다.
    </p>
