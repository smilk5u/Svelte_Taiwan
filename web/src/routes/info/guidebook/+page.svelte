        대만을 흠뻑 느끼길 원한다면 대만의 남쪽지역에 위치한 컨딩(墾丁)의 아름다운
        해변을 추천한다. <br />
        이곳에서는 연중 내내 한 여름의 정취를 감상할 수 있다.
